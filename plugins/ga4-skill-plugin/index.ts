import type {LoadContext, Plugin} from '@docusaurus/types';

type PluginOptions = {
  measurementId: string;
  gtmContainerId: string;
  bookId: string;
  consentMode: 'always_on' | 'balanced_by_region' | 'strict_by_default';
  restrictedRegions: string[];
};

export default function ga4SkillPlugin(_context: LoadContext, options: PluginOptions): Plugin {
  const {measurementId, gtmContainerId, bookId, consentMode, restrictedRegions} = options;

  const initScript = `
(function() {
  if (window.__ga4SkillTagLoaded) return;
  window.__ga4SkillTagLoaded = true;

  window.dataLayer = window.dataLayer || [];
  function gtag(){ window.dataLayer.push(arguments); }
  window.gtag = window.gtag || gtag;

  var restricted = new Set(${JSON.stringify(restrictedRegions)});
  var locale = (navigator.languages && navigator.languages[0]) || navigator.language || '';
  var regionMatch = locale.match(/-([A-Za-z]{2})$/);
  var inferredRegion = regionMatch ? regionMatch[1].toUpperCase() : null;

  var analyticsStorage = 'granted';
  if (${JSON.stringify(consentMode)} === 'strict_by_default') {
    analyticsStorage = 'denied';
  } else if (${JSON.stringify(consentMode)} === 'balanced_by_region') {
    analyticsStorage = inferredRegion && restricted.has(inferredRegion) ? 'denied' : 'granted';
  }

  var gtagScript = document.createElement('script');
  gtagScript.async = true;
  gtagScript.src = 'https://www.googletagmanager.com/gtag/js?id=' + ${JSON.stringify(measurementId)};
  document.head.appendChild(gtagScript);

  window.bookAnalyticsContext = {
    book_id: ${JSON.stringify(bookId)},
    version: 'v2',
    measurement_id: ${JSON.stringify(measurementId)}
  };

  gtag('consent', 'default', {
    analytics_storage: analyticsStorage,
    ad_storage: 'denied',
    ad_user_data: 'denied',
    ad_personalization: 'denied'
  });

  gtag('js', new Date());
  gtag('config', ${JSON.stringify(measurementId)}, {
    send_page_view: true,
    anonymize_ip: true,
  });

  window.bookAnalyticsTrack = function(eventName, params) {
    var payload = Object.assign({}, params || {}, {
      event_name: eventName,
      book_id: window.bookAnalyticsContext.book_id,
      page_path: window.location.pathname,
      version: window.bookAnalyticsContext.version
    });

    gtag('event', eventName, payload);
    window.dataLayer.push(Object.assign({ event: eventName }, payload));
  };

  // Keep GTM for existing container workflows.
  (function(w, d, s, l, i) {
    w[l] = w[l] || [];
    w[l].push({ 'gtm.start': new Date().getTime(), event: 'gtm.js' });
    var f = d.getElementsByTagName(s)[0],
      j = d.createElement(s),
      dl = l !== 'dataLayer' ? '&l=' + l : '';
    j.async = true;
    j.src = 'https://www.googletagmanager.com/gtm.js?id=' + i + dl;
    f.parentNode.insertBefore(j, f);
  })(window, document, 'script', 'dataLayer', ${JSON.stringify(gtmContainerId)});
})();
`;

  const noScript = `<iframe src="https://www.googletagmanager.com/ns.html?id=${gtmContainerId}" height="0" width="0" style="display:none;visibility:hidden"></iframe>`;

  return {
    name: 'ga4-skill-plugin',
    injectHtmlTags() {
      return {
        headTags: [
          {
            tagName: 'script',
            innerHTML: initScript,
          },
        ],
        preBodyTags: [
          {
            tagName: 'noscript',
            innerHTML: noScript,
          },
        ],
      };
    },
  };
}
